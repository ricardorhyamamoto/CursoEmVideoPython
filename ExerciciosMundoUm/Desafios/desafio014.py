# ESCREVA UM PROGRAMA QUE CONVERTA UMA TEMPERATURA DIGITADA EM GRAUS CELSIUS PARA GRAUS FAHRENHEIT

celsius = float(input('Informe a temperatura em graus Celsius (ºC): '))

fahrenheit = 1.8 * celsius + 32

print('{}ºC graus Celsius é igual a {}ºF graus Fahrenheit'.format(celsius, fahrenheit))
